    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String titulo = txtTitulo.getText().trim();
        String descripcion = txtDescripcion.getText().trim();

        // Validaciones
        if (titulo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El título es obligatorio", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (dateChooser.getDate() == null) {  // Usar dateChooser
            JOptionPane.showMessageDialog(this, "La fecha límite es obligatoria", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Convertir Date a LocalDate
        java.util.Date fechaSeleccionada = dateChooser.getDate();
        LocalDate fechaLimite = new java.sql.Date(fechaSeleccionada.getTime()).toLocalDate();

        // Guardar o actualizar
        if (tareaEditar == null) {
            // Nueva tarea
            Tarea nuevaTarea = new Tarea(titulo, descripcion, fechaLimite, usuarioActual.getId());
            if (repositorio.crearTarea(nuevaTarea)) {
                JOptionPane.showMessageDialog(this, "Tarea creada correctamente");
                ventanaPrincipal.actualizarTabla();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Error al crear tarea", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Editar tarea existente
            tareaEditar.setTitulo(titulo);
            tareaEditar.setDescripcion(descripcion);
            tareaEditar.setFechaLimite(fechaLimite);

            if (repositorio.actualizarTarea(tareaEditar)) {
                JOptionPane.showMessageDialog(this, "Tarea actualizada correctamente");
                ventanaPrincipal.actualizarTabla();
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar tarea", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


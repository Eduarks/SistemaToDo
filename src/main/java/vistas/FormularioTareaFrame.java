    private void configurarDateChooser() {
        dateChooser = new com.toedter.calendar.JDateChooser();
        dateChooser.setDateFormatString("dd/MM/yyyy");
        dateChooser.setFont(new java.awt.Font("Segoe UI Variable", 0, 12));
        dateChooser.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(220, 220, 220), 1, true));

        //Reemplazar el txtFechaLimite visualmente
        //Obtener el contenedor padre de txtFechaLimite
        java.awt.Container parent = txtFechaLimite.getParent();

        //Guardar las restricciones de layout
        java.awt.LayoutManager layout = parent.getLayout();
        Object constraints = null;

        if (layout instanceof javax.swing.GroupLayout) {
            // Para GroupLayout, copiar las dimensiones
            dateChooser.setPreferredSize(txtFechaLimite.getPreferredSize());
            dateChooser.setBounds(txtFechaLimite.getBounds());
        }

        //Remover el campo de texto original
        parent.remove(txtFechaLimite);

        // Agregar el date chooser en la misma posición
        parent.add(dateChooser);

        // Actualizar el layout
        parent.revalidate();
        parent.repaint();
    }
    
